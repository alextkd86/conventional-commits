/**
 * Add a + b
 * @param {*} a 
 * @param {*} b 
 * @returns 
 */
const add = (a: number, b: number): number => {
    return a + b;
};

/**
 * Subtract a - b
 * @param {*} a 
 * @param {*} b 
 * @returns 
 */
const subtract = (a: number, b: number): number => {
    return a -b;
}

export { add, subtract };